    public int generaroperacion(int a, int b, int reto){

        int result = 0;

        if (reto == 0){
            String a_str = Integer.toString(a);
            String b_str = Integer.toString(b);
            result = a + b;
            jTextField2.setText(a_str + " + " + b_str );

        }else if (reto == 1){
            String a_str = Integer.toString(a);
            String b_str = Integer.toString(b);
            result = a - b;
            jTextField2.setText(a_str + " - " + b_str );

        }else if (reto == 2){
            String a_str = Integer.toString(a);
            String b_str = Integer.toString(b);
            result = a * b;
            jTextField2.setText(a_str+" x "+b_str);

        }else {
            String a_str = Integer.toString(a);
            String b_str = Integer.toString(b);
            result = a / b;
            jTextField2.setText(a_str+" / "+b_str);
        }
        return result;
    }

